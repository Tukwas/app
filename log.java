    private void log1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_log1ActionPerformed
        uname = jname.getText().trim();
        password = new String(jpas.getPassword()).trim();
        selectedRole = ((String) role.getSelectedItem()).toLowerCase();

        if (uname.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username and password cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        JSONArray userList = selectedRole.equals("teacher") ? teacherList : studentList;
        boolean isAuthenticated = authenticateUser(userList);

        // Additional check for Teacher role misselection
        if (!isAuthenticated) {
            JSONArray alternateList = selectedRole.equals("teacher") ? studentList : teacherList;
            boolean isInAlternateRole = authenticateUser(alternateList);
            if (isInAlternateRole) {
                JOptionPane.showMessageDialog(this, "You are registered as a "
                        + (selectedRole.equals("teacher") ? "Student" : "Teacher")
                        + ". Please select the correct role.", "Login Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Additional check for Teacher role misselection
            if (!isAuthenticated && authenticateUser(teacherList)) {
                JOptionPane.showMessageDialog(this, "You are registered as a Teacher. Please select the correct role.",
                        "Login Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            JOptionPane.showMessageDialog(this, "Invalid Username or Password. Please try again.",
                    "Login Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (isAuthenticated) {
            // Non-blocking success message
            
            JDialog dialog = new JDialog(this, "Success", true);
            JLabel messageLabel = new JLabel("Login Successfully", JLabel.CENTER);
            messageLabel.setFont(new Font("Arial", Font.PLAIN, 14));
            dialog.add(messageLabel);
            dialog.setSize(300, 150);
            dialog.setLocationRelativeTo(this);

// Set timer to automatically close the dialog and redirect
            Timer timer = new Timer(1000, e -> {
                dialog.dispose(); // Close the message dialog after 1 second
                // Redirect to the appropriate panel
                if ("teacher".equals(selectedRole)) {
                    adminPanels ad = new adminPanels(uname);
                    ad.setVisible(true);
                    ad.pack();
                    ad.setLocationRelativeTo(null);
                } else if ("student".equals(selectedRole)) {
                    studentPanels st = new studentPanels(uname);
                    st.setVisible(true);
                    st.pack();
                    st.setLocationRelativeTo(null);
                    this.dispose();
                }
                 // Close the login form
            });
            timer.setRepeats(false); // Ensure the timer only runs once
            timer.start();

            dialog.setVisible(true); // Display the dialog
        }
    }//GEN-LAST:event_log1ActionPerformed

