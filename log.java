    private boolean authenticateUser(JSONArray userList) {
        for (Object obj : userList) {
            JSONObject user = (JSONObject) obj;
            String userName = (String) user.get("username");
            String userPass = (String) user.get("password");

            if (uname.equals(userName) && password.equals(userPass)) {
                return true;
            }
        }
        return false;
    }

    private void rememberUser(String user, String pass, String role) {
        try (FileWriter file = new FileWriter("remember.json")) {
            JSONObject obj = new JSONObject();
            obj.put("username", user);
            obj.put("password", pass);
            obj.put("role", role);
            file.write(obj.toJSONString());
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void loadRememberedUser() {
        File file = new File("remember.json");
        if (!file.exists()) {
            return;
        }

        try (FileReader reader = new FileReader(file)) {
            JSONObject obj = (JSONObject) opener.parse(reader);
            jname.setText((String) obj.get("username"));
            jpas.setText((String) obj.get("password"));
            role.setSelectedItem((String) obj.get("role"));
            remember.setSelected(true);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void forgetUser() {
        File file = new File("remember.json");
        if (file.exists()) {
            file.delete();
        }
    }

